# Let's Compare Using  operator ( < and > )
x = 2
y = 3

print (x > y)   #False
print ( x < y)  #True

var = "Test"
var2 = "Tasty"

print ( var > var2)  #True
print ( var < var2)  #False

# Let's Compare Using  operator ( <= and >= )
#Greater than and smaller than operators can also be used to compare strings lexicographically,
# (the alphabetical order of words is based on the alphabetical order of their component letters).
print ("===========================================")
x = 3
y = 3

print (x >= y)   #False
print ( x <= y)  #True

var = "Test"
var2 = "Tasty"

print ( var >= var2)  #True
print ( var <= var2)  #False
