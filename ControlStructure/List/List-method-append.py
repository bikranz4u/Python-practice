# Another way of altering/modifying lists is using the "append" method. 
# This adds an item to the "end"of an existing list.

nums = [1, 2, 3] #append method appends at the end of the list
print(nums)
nums.append(4)
print(nums)
nums.append(0)
print(nums)

#The dot before append is there because it is a method of the list class. 