#max(list): Returns the list item with the maximum value
#min(list): Returns the list item with minimum value
letters = ['p', 'q', 'r', 's', 'p', 'u']
print (max(letters))
print (min(letters))   #O/P:- u 
print("==============")

letters1 = ['a', 'q', 'u', 's', 'p', 'r']
print (max(letters1))   #O/P:- u 
print (min(letters1))
print("==============")

nums = [0, 1, 5, 3, 4, -1, 5.0]
print (max(nums)) 
print (min(nums))
