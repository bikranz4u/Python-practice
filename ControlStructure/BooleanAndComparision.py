#There are two Boolean values: True and False.
# They can also be created by comparing values, for instance by using the equal operator ==.

my_boolean = True

print (my_boolean)   #True

x = 2
y = 3
print ( x == y)      # False

# Another comparison operator, the not equal operator (!=), 
# evaluates to True if the items being compared aren't equal, and False if they are.

print ( x != y)      # True

var = "Eleven"

var2 = "eleven"

print ( var == var2)   # False