#As with integers and floats, strings in Python can be added, 
#using a process called concatenation, which can be done on any two strings. 
#When concatenating strings, it doesn't matter whether they've been created with single or double quotes.
print ("Spam" + 'eggs') # O/P :Spameggs

print ("First string" + ", " + "second string")

