str=input("Enter a String: ")
#upper() -Converts lowercase letters in string to uppercase.
print('This is converted to Upper Case:-',str.upper())

#lower() - Converts all uppercase letters in string to lowercase.
print('This is converted to Lower Case:-',str.lower())

#swapcase() - Inverts case for all letters in string. I,e Small to Caps and Vice-Versa
print('Case Is reversed:-',str.swapcase())

#title()
print('Every Word\'s First letter is Capital:-',str.title())

#capitalize()- Only First letter will be Capital, other will be small letter
print('This is Capitalize Method:-',str.capitalize())