#To determine the quotient and remainder of a division, use the floor division (//) 
#and modulo (%) operators, respectively. 
#These operators can be used with both floats and integers.

print (20 / 3)  # It is a division
print (20 // 3) # The result shows Quotient as 6
print (20 % 3)  # The result shows  Reminder 2

print (7%(5 // 2)) # The result goes like 7 % 2, resulting output 1